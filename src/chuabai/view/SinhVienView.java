    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        SinhVien sv = new SinhVien();
        String tenSinhVien = txtTenSinhVien.getText();
        Double diem = Double.parseDouble(txtDiem.getText().toString());
        Boolean gioiTinh = false;
        if (rdNam.isSelected()) {
            gioiTinh = true;
        }
        String tenLop = cbLop.getSelectedItem().toString();
        sv.setTenSv(tenSinhVien);
        sv.setDiem(diem);
        sv.setGioiTinh(gioiTinh);
        sv.setTenLop(tenLop);

        if (sinhVienService.them(sv)) {
            JOptionPane.showMessageDialog(this, "them Thanh cong ");
            loadData(sinhVienService.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them That Bai");
        }
    }//GEN-LAST:event_btnThemActionPerformed

