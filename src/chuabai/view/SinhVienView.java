    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tbSinhVien.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "hay Chon dong can xoa");
            return;
        }
        if (sinhVienService.xoa(index)) {
            JOptionPane.showMessageDialog(this, "xoa Thanh cong");
            loadData(sinhVienService.getList());
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tbSinhVien.getSelectedRow();
        SinhVien sv = new SinhVien();
        String tenSinhVien = txtTenSinhVien.getText();
        Double diem = Double.parseDouble(txtDiem.getText().toString());
        Boolean gioiTinh = false;
        if (rdNam.isSelected()) {
            gioiTinh = true;
        }
        String tenLop = cbLop.getSelectedItem().toString();
        sv.setTenSv(tenSinhVien);
        sv.setDiem(diem);
        sv.setGioiTinh(gioiTinh);
        sv.setTenLop(tenLop);
        if (sinhVienService.update(row, sv)) {
            JOptionPane.showMessageDialog(this, "update thanh cong");
            loadData(sinhVienService.getList());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tbSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSinhVienMouseClicked
        int row = tbSinhVien.getSelectedRow();
        txtTenSinhVien.setText(tbSinhVien.getValueAt(row, 0).toString());
        txtDiem.setText(tbSinhVien.getValueAt(row, 1).toString());
        cbLop.setSelectedItem(tbSinhVien.getValueAt(row, 2));
        if (tbSinhVien.getValueAt(row, 3).toString().equalsIgnoreCase("Nữ")) {
            rdNu.setSelected(true);
        } else {
            rdNam.setSelected(true);
        }


    }//GEN-LAST:event_tbSinhVienMouseClicked

